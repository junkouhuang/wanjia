var currentPage=0;
//标识是否可点击
var sliderWidth;
var flag=1;
window.onload=function(){
	//页面初始化
	 sliderWidth=$(".slideToolbar").width();//父容器宽度
	loadPageContent();
	
	$(".f2").bind("click",function(){
		$(".f2").removeClass("active");
		$(this).addClass("active");
		currentPage=$(this).index();
		$(".prev,.next").removeClass("last")
		$(".prev").addClass("last");
		$(".slideToolbar").animate({marginLeft:0});
		loadPageContent();
	})

	

	var oDiv="";
		var pge=""
	function loadPageContent(){
		
	 $(".title").text($(".f2").eq(currentPage).text());
		if(window.screen.width>=768){
	          
				
				for(j in page[currentPage]){
					var oDivChild="";
					for(i in page[currentPage][j]){
						 oDivChild +='<div class="col-xs-6 col-sm-4 col-md-4 col-lg-4 "> '+
									'<img src="'+page[currentPage][j][i].url+'"/>'+
					            '</div>'
					}
					oDiv += '<div class="row">'+
						oDivChild+
						'</div>';
						pge+='<li onclick="switcher(this)">'+(parseInt(j)+1)+'</li>'
		          }
		}else{
	   		
				for(j in page[currentPage]){
					var oDivChild="";
					for(i in page[currentPage][j]){
						 oDivChild +='<div class="col-xs-6 col-sm-4 col-md-4 col-lg-4 "> '+
									'<img src="'+page[currentPage][j][i].url+'"/>'+
					            '</div>'
					}

		           }
					oDiv += '<div class="row">'+
						oDivChild+
						'</div>';
						pge+='<li onclick="switcher(this)">'+(parseInt(j)+1)+'</li>'
		}
		
		$("#slideToolbar").html(oDiv);
		$("#page").html(pge);
		$("#page li").eq(0).addClass("sin");
		
		//var sliderWidth=0;
		var num=$(".slideToolbar .row").length;//子元素个数

		
		if(window.screen.width>=768){
				
			
				sliderHeight=$(".slideToolbar .row").eq(0).height();
				//父容器宽高
		
				$(".slideToolbar").width(num*2*sliderWidth+"px")
				$(".slideToolbar").height("auto")
				//子容器宽高
				$(".slideToolbar .row").width(sliderWidth+"px")
				$(".slideToolbar .row").height("auto")
		}
		//mobile
		else{
			
		}


	}

    
    //上一页prev
	$(".prev").bind("click",function(){
		if(flag>1){
			flag=flag-1;
			$(".slideToolbar").animate({marginLeft:"-"+sliderWidth*(flag-1)+"px"},500,"linear")
			$(".prev,.next").removeClass("last")
			if(flag==1){
				$(this).addClass("last");
			}
			sin();
		}
	
	})
	
	
	//下一页next
	$(".next").bind("click",function(){
		if(flag<$(".slideToolbar .row").length){
			flag=flag+1;
			$(".slideToolbar").animate({marginLeft:"-"+sliderWidth*(flag-1)+"px"},500,"linear")
			$(".prev,.next").removeClass("last")
			if(flag==$(".slideToolbar .row").length){
				$(this).addClass("last");
			}
			sin()
		}
	})
	
	
	
	//页码事件
	/*$(".page li").bind("click",function(){	
		
	})*/
	

	
	
    
    $("#more").bind("click",function(){
    	if(flag<page.length){
    		var oDivChild="";
			for(j in page[flag]){
	           var oDivChild="";
					for(i in page[flag][j]){
						 oDivChild +='<div class="col-xs-6 col-sm-4 col-md-4 col-lg-4 "> '+
									'<img src="'+page[flag][j][i].url+'"/>'+
					            '</div>'
					}
	           }
			oDiv += '<div class="row">'+
					oDivChild+
					'</div>';
			flag=flag+1;
	    	$("#slideToolbar").append(oDiv);
    	}
    	else{
    		$("#more").html("已经加载全部")
    	}
    	
    })
}


function switcher(e){
	flag=parseInt($(e).text());
	$(".slideToolbar").animate({marginLeft:"-"+sliderWidth*(flag-1)+"px"},500,"linear")
	$(".prev,.next").removeClass("last")
	if(flag==1){
		$(".prev").addClass("last");
	}
	if(flag==$(".slideToolbar .row").length){
			$(".next").addClass("last");
		}
	sin();
}



function sin(){
	$(".page li").removeClass("sin");
	$(".page li").eq(flag-1).addClass("sin");
}



























